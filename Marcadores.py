#!/usr/bin/python3

#   Estudo sobre marcadores e sua forma de funcionar.

a = 42
print ("o numero %d é muito legal" %a)

b = 13
print ("o numero", b, "é muito legal")

c = "como"
print ("algo %s isso" %c)

